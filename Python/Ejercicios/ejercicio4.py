# Dados dos números, mostrar la suma, resta, división y multiplicación de ambos.

num1 = int(input("Introduce el primer valor:"))
num2 = int(input("Introduce el segundo valor:"))

print("La suma de los valores introducidos es: ",(num1 + num2))
print("La resta de los valores introducidos es: ",(num1 - num2))
print("La multiplicación de los valores introducidos es: ",(num1 * num2))
print("La división de los valores introducidos es: ",(num1 / num2))